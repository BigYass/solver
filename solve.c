unsigned int rotate_right(unsigned int value, int count){
  return (value >> count) | (value << (32 - count));
}

unsigned int rotate_left(unsigned int value, int count){
  return (value << count) | (value >> (32 - count));
}

unsigned int checksum(char opcode[]){
  unsigned int result = 0;
  for (int i = 0; i < OPCODE_SIZE; i++){
    result = ((result + opcode[i] & 0xFF) & 0xFF) + (result >> 8 << 8); 
    result = rotate_left(result, 3);
  }

  return result;
}

